public class Movies {
    private String title;
    private int rating;
    private int duration;

    public Movies()
    {
        title = "";
        rating = 0;
    }
    public void setDuration(int d)
    {
        duration = d;
    }
    public int getDuration()
    {
        return duration;
    }
    public void setTitle3(String t){
        title = t;
    }
    public String getTitle3()
    {
        return title;
    }
    public void setRating3(int t){
        rating = t;
    }
    public int getRating3()
    {
        return rating;
    }

}
