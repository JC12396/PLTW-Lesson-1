public class Books {
    private String title;
    private int rating;

    public Books()
    {
        title = "";
        rating = 0;
    }
    public void setTitle2(String t){
        title = t;
    }
    public String getTitle2()
    {
        return title;
    }
    public void setRating2(int t){
        rating = t;
    }
    public int getRating2()
    {
        return rating;
    }

}
